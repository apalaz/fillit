#include "libft.h"

char		*ft_strdup(const char *src)
{
	int		i;
	char	*dst;

	dst = (char*)malloc(sizeof(*dst) * (ft_strlen(src) + 1));
	if (dst == NULL)
	{
		errno = ENOMEM;
		return (NULL);
	}
	i = 0;
	while (src[i] != '\0')
	{
		dst[i] = src[i];
		i++;
	}
	dst[i] = src[i];
	return (dst);
}
