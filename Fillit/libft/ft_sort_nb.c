#include "libft.h"

int		*ft_sort_nb(int *nb, int size)
{
	int i;

	i = 0;
	while (i < size - 1)
	{
		if (nb[i] <= nb[i + 1])
			i++;
		else
		{
			ft_swap_nb(&nb[i], &nb[i + 1]);
			i = 0;
		}
	}
	return (nb);
}
